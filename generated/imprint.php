        <p>Sia la formazione che il valore di ´"Gioca in attacco" determinano il grado di offensività e quindi di rischio del gioco. È consigliabile regolare questo valore a seconda dell'avversario.</p>

        <p><strong>Schieramento dei giocatori</strong></p>
        <p>>È possibile trascinare i giocatori sul campo di gioco con il mouse. In alternativa, si possono premere i pulsanti corrispondenti o fare doppio clic sulla riga del giocatore desiderato. Il doppio clic funziona solo se il giocatore ha una posizione principale e questa non è ancora occupata.</p>

    	<p><strong>Giocatore sostituto</strong></p>
    	<p>È possibile mettere in panchina fino a cinque giocatori facendo clic sul simbolo della panchina accanto ai giocatori. Questo permette di cambiare i giocatori durante la partita, ad esempio in caso di infortuni o per motivi tattici.</p>

